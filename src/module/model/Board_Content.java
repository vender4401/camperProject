package module.model;

public class Board_Content {
	private int number;
	private String content;
	
	public Board_Content(int number, String content) {
		super();
		this.number = number;
		this.content = content;
	}

	public int getNumber() {
		return number;
	}

	public String getContent() {
		return content;
	}
	
	
}
